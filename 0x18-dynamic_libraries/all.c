#include <stdio.h>
/**
 * sub - sub towo numbers
 * @a: first
 * @b: second
 * Return: nothing
 */

int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - div towo numbers
 * @a: first
 * @b: second
 * Return: nothing
 */

int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod towo numbers
 * @a: first
 * @b: second
 * Return: nothing
 */

int mod(int a, int b)
{
	return (a % b);
}
/**
 * add - add towo numbers
 * @a: first
 * @b: second
 * Return: nothing
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * mul - mul towo numbers
 * @a: first
 * @b: second
 * Return: nothing
 */

int mul(int a, int b)
{
	return (a * b);
}
